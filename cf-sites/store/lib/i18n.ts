const languageMap: Record<string, StoreMessages> = {
  'zh-CN': zh,
  zh,
  en,
  id,
  ja,
  ko,
  vi,
  'vi-VN': vi,
  th,
  'th-TH': th,
};

const defaultCurrencyByLanguage: Record<string, string> = {
  'zh-CN': 'CNY',
  zh: 'CNY',
  en: 'USD',
  id: 'IDR',
  ja: 'JPY',
  ko: 'KRW',
  vi: 'VND',
  'vi-VN': 'VND',
  th: 'THB',
  'th-TH': 'THB',
};

export const supportedStoreLanguages = Object.keys(languageMap);

export function resolveStoreLanguage(language?: string | null): string {
  const normalized = language?.trim();
  if (!normalized) return 'en';

  if (languageMap[normalized]) return normalized;

  const base = normalized.split('-')[0];
  if (languageMap[base]) return base;

  return 'en';
}

export function resolveStoreMessages(language?: string | null): StoreMessages {
  return languageMap[resolveStoreLanguage(language)] || en;
}

export function resolveCurrency(language?: string | null, currencyCode?: string | null): string {
  if (currencyCode) return currencyCode.toUpperCase();
  return defaultCurrencyByLanguage[resolveStoreLanguage(language)] || 'USD';
}

export function formatPrice(amount: number, language?: string | null, currencyCode?: string | null): string {
  const locale = resolveStoreLanguage(language);
  const currency = resolveCurrency(language, currencyCode);

  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency,
  }).format(amount);
}
